package s1;
/*
 * Simple program that prints the average/sum/product
 * of 6 decimal point numbers
 */
public class Lab2_141_2 

{
	public static void main(String args[])
	{
		
	double a=1.1;
	double b=1.2;
	double c=1.3;
	double d=1.4;
	double e=1.5;
	double f=1.6;
	double g=(a+b+c+d+e+f)/6;
	double h=(a+b+c+d+e+f);
	double i=(a*b*c*d*e*f);
	
	System.out.println("The average is: "+g+ "\nThe sum is: " +h+ "\nThe product is: "+i);
	
	
	
	
	
	}
}
