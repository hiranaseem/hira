package s1;
/*
 * Java program that uses a while-loop to print the numbers from 100 to 999 to the screen on the same line, 
 * separated by three spaces.
 */
public class Lab5_141_1 

{	
	public static void main(String args[])
	{
		int x=100;
		while(x<=999)
		{
			System.out.print(x+ "   ");
			x++;
		}
	}

}
