package s1;
/*
 * program that uses a for-loop to print the numbers from 100 to 999 to the screen in order, 
 * separated by three spaces.
 */

public class Lab6_141_1 

{
	public static void main(String args[])
	{
		for(int i =100; i<=999; i++)
		{
			System.out.print(i+"   ");
		}
	}

}
