package s1;
/*
 * Java program that prints out all numbers from 50 to 150 in steps of 3 (50, 53, 56,…) 
 * using a while loop.
 */
public class Lab4_141_5 
{
	
	public static void main(String args[])
	{
		int x=50;
		
		while(x<=150)
		{
			System.out.println(x);
			x=x+3;
		}
			
	}
}
